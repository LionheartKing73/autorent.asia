<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><u><span style='color:red'>Cultural Sites in
Australia</span></u></b></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>&nbsp;</span></b></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:red'>Sydney , New South Wales: </span><span
style='color:black'>Home to indigenous people for tens of thousands of years,
Sydney's European history began when Captain Cook laid eyes on Botany Bay in
1770. The discovery of gold in 1851 led to massive growth and Sydney has
blossomed into a thrilling, glittering, modern city with world-class arts and
culture institutions.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:red'>Melbourne, Victoria:&nbsp; </span><span
style='color:black'>The state of Victoria has a proud and fascinating history,
reminders of which remain on display in lovely, laid-back Melbourne. Wealth
from the gold rush has left a legacy of fine architecture, parks adorn every
area of the city and innovative museums like the Melbourne Museum and the
Melbourne Gaol captivate all comers.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:red'>Alice Springs, Red Centre,
Northern Territory: </span><span style='color:black'>This hardy desert town of
26,000 started life as a telegraph station named Stuart in the mid 1800s.
Renamed Alice Springs in 1933, it became an important WWII staging post. The
Capital of the Australian Outback has been the home of the aboriginal Arrernte
people for 50,000 years and is a base for visits to Uluru (Ayers Rock). </span><span
style='color:red'><br/>
<br/>
</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><u><span style='color:red'>World heritage Sites
of Australia:</span></u></b></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:red'>Gondwana Rainforests of
Australia: </span><span style='color:black'>This site, comprising several
protected areas, is situated predominantly along the Great Escarpment on
Australia's east coast. The outstanding geological features displayed around
shield volcanic craters and the high number of rare and threatened rainforest
species are of international significance for science and conservation.&nbsp;</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:red'>Australian Fossil Mammal
Sites (Riversleigh / Naracoorte): </span><span style='color:black'>Riversleigh
and Naracoorte, situated in the north and south respectively of eastern
Australia, are among the world's 10 greatest fossil sites. They are a superb
illustration of the key stages of evolution of Australia's unique fauna.&nbsp; </span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:red'>Fraser Island:&nbsp; </span><span
style='color:black'>Fraser Island lies just off the east coast of Australia. At
122 km long, it is the largest sand island in the world. Majestic remnants of
tall rainforest growing on sand and half the world's perched freshwater dune
lakes are found inland from the beach. The combination of shifting sand-dunes,
tropical rainforests and lakes makes it an exceptional site.&nbsp; </span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:red'>Great Barrier Reef,
Queensland:&nbsp; </span><span style='color:black'>The Great Barrier Reef is a
site of remarkable variety and beauty on the north-east coast of Australia. It
contains the world's largest collection of coral reefs, with 400 types of
coral, 1,500 species of fish and 4,000 types of mollusc. It also holds great
scientific interest as the habitat of species such as the dugong ('sea cow')
and the large green turtle, which are threatened with extinction.&nbsp; </span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:red'>Heard and McDonald Islands:
</span><span style='color:black'>Heard Island and McDonald Islands are located
in the Southern Ocean, approximately 1,700 km from the Antarctic continent and
4,100 km south-west of Perth. As the only volcanically active subantarctic
islands they 'open a window into the earth', thus providing the opportunity to
observe ongoing geomorphic processes and glacial dynamics. The distinctive
conservation value of Heard and McDonald -- one of the world's rare pristine
island ecosystems -- lies in the complete absence of alien plants and animals,
as well as human impact.&nbsp; </span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:red'>Kakadu National Park: </span><span
style='color:black'>This unique archeological and ethnological reserve, located
in the Northern Territory, has been inhabited continuously for more than 40,000
years. The cave paintings, rock carvings and archeological sites record the
skills and way of life of the region's inhabitants, from the hunter-gatherers
of prehistoric times to the Aboriginal people still living there. It is a
unique example of a complex of ecosystems, including tidal flats, floodplains,
lowlands and plateaux, and provides a habitat for a wide range of rare or
endemic species of plants and animals.&nbsp; </span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:red'>Lord Howe Island Group: </span><span
style='color:black'>A remarkable example of isolated oceanic islands, born of
volcanic activity more than 2,000 m under the sea, these islands boast a
spectacular topography and are home to numerous endemic species, especially
birds.&nbsp; </span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:red'>Macquarie Island: </span><span
style='color:black'>Macquarie Island (34 km long x 5 km wide) is an oceanic
island in the Southern Ocean, lying 1,500 km south-east of Tasmania and
approximately halfway between Australia and the Antarctic continent. The island
is the exposed crest of the undersea Macquarie Ridge, raised to its present
position where the Indo-Australian tectonic plate meets the Pacific plate. It
is a site of major geoconservation significance, being the only place on earth
where rocks from the earth's mantle (6 km below the ocean floor) are being
actively exposed above sea-level. These unique exposures include excellent
examples of pillow basalts and other extrusive rocks.&nbsp;</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:red'>Purnululu National
Park:&nbsp; </span><span style='color:black'>The 239,723 ha Purnululu National
Park is located in the State of Western Australia. It contains the deeply
dissected Bungle Bungle Range composed of Devonian-age quartz sandstone eroded
over a period of 20 million years into a series of beehive-shaped towers or
cones, whose steeply sloping surfaces are distinctly marked by regular
horizontal bands of dark-gray cyanobacterial crust (single-celled
photosynthetic organisms). These outstanding examples of cone karst owe their
existence and uniqueness to several interacting geological, biological,
erosional and climatic phenomena.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:red'>Royal Exhibition Building
and Carlton Gardens: </span><span style='color:black'>The Royal Exhibition
Building and its surrounding Carlton Gardens were designed for the great
international exhibitions of 1880 and 1888 in Melbourne. The building and
grounds were designed by Joseph Reed. The building is constructed of brick and
timber, steel and slate. It combines elements from the Byzantine, Romanesque,
Lombardic and Italian Renaissance styles. The property is typical of the
international exhibition movement which saw over 50 exhibitions staged between
1851 and 1915 in venues including Paris, New York, Vienna, Calcutta, Kingston
(Jamaica) and Santiago (Chile). All shared a common theme and aims: to chart
material and moral progress through displays of industry from all nations.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:red'>Shark Bay, Western
Australia: </span><span style='color:black'>At the most westerly point of the
Australian continent, Shark Bay, with its islands and the land surrounding it,
has three exceptional natural features: its vast sea-grass beds, which are the
largest (4,800 km2) and richest in the world; its dugong ('sea cow')
population; and its stromatolites (colonies of algae which form hard,
dome-shaped deposits and are among the oldest forms of life on earth). Shark
Bay is also home to five species of endangered mammals.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:red'>Sydney Opera House: </span><span
style='color:black'>Inaugurated in 1973, the Sydney Opera House is a great
architectural work of the 20th century that brings together multiple strands of
creativity and innovation in both architectural form and structural design. A
great urban sculpture set in a remarkable waterscape, at the tip of a peninsula
projecting into Sydney Harbour, the building has had an enduring influence on
architecture. The Sydney Opera House comprises three groups of interlocking
vaulted 'shells' which roof two main performance halls and a restaurant. These
shell-structures are set upon a vast platform and are surrounded by terrace
areas that function as pedestrian concourses. In 1957, when the project of the
Sydney Opera House was awarded by an international jury to Danish architect
J&oslash;rn Utzon, it marked a radically new approach to construction.&nbsp;</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:red'>Tasmanian Wilderness: </span><span
style='color:black'>In a region that has been subjected to severe glaciation,
these parks and reserves, with their steep gorges, covering an area of over 1
million ha, constitute one of the last expanses of temperate rainforest in the
world. Remains found in limestone caves attest to the human occupation of the
area for more than 20,000 years.&nbsp;</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:red'>Uluru-Kata Tjuta National
Park: </span><span style='color:black'>This park, formerly called Uluru (Ayers
Rock -- Mount Olga) National Park, features spectacular geological formations
that dominate the vast red sandy plain of central Australia. Uluru, an immense
monolith, and Kata Tjuta, the rock domes located west of Uluru, form part of
the traditional belief system of one of the oldest human societies in the
world. The traditional owners of Uluru-Kata Tjuta are the Anangu Aboriginal
people.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:red'>Wet Tropics of Queensland: </span><span
style='color:black'>This area, which stretches along the north-east coast of
Australia for some 450 km, is made up largely of tropical rainforests. This
biotope offers a particularly extensive and varied array of plants, as well as
marsupials and singing birds, along with other rare and endangered animals and
plant species.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:red'>Willandra Lakes Region: </span><span
style='color:black'>The fossil remains of a series of lakes and sand formations
that date from the Pleistocene can be found in this region, together with
archeological evidence of human occupation dating from 45--60,000 years ago. It
is a unique landmark in the study of human evolution on the Australian
continent. Several well-preserved fossils of giant marsupials have also been
found here.</span></p>

