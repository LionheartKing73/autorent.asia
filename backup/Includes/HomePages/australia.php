<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Australia has more than
beaches than you can shake a stick at......so we have just listed a few
favourites.</span></b></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Sydney New South
Wales:&nbsp; </span></b><span style='color:black'>With dozens of stunning ocean
beaches within half an hour of downtown, there are few better urban beach
destinations than Sydney. Famed Bondi Beach sees up to 40,000 on busy weekends.
If quieter beaches appeal, take the coastal walk past smaller strands all the
way to Coogee's calmer sands.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Byron Bay New South
Wales:&nbsp; </span></b><span style='color:black'>Byron's legendary,
lighthouse-dotted, white sand beaches are some of Australia's best. Situated
where the Coral Sea meets the Tasman Sea, these clean, unpolluted, laid-back
beaches remain relatively undeveloped and are great places to spot bottlenose
dolphins, rays, whales and sea turtles.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Surfers Paradise, Gold Coast,
Queensland:&nbsp; </span></b><span style='color:black'>A perfect little beach
town of 18,500 on Australia's east coast, 48 miles from Brisbane, Surfer's
Paradise sits amid seemingly endless stretches of pristine sand. Nature has
conspired to create amazing conditions and the crashing waves teem with
swimmers as well as novice and more advanced surfers.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Perth, Western
Australia:&nbsp; </span></b><span style='color:black'>Perth's active beach
scene is dominated by two main stretches: Civilized Cottesloe, just south of
town, is the choice for families, swimmers and snorkelers. More lively
Scarborough, just north of town, is by day popular with surfers (via water,
wind and kite), and by night is a spirited beachside club scene.</span></p>

