<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><u><span lang=EN-GB style='color:red'>National
Parks in Sri Lanka </span></u></b></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span lang=EN-GB style='color:black'>&nbsp;</span></b></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>BUNDALA NATIONAL PARK</span></b><span
style='color:black'>, covering an area of 6216 hectares, is located about 250km
southeast of Colombo in the Hambantota district. The park falls within the
southeastern arid zone of Sri Lanka, with a general climate that can be
classified as hot and dry.&nbsp; The terrain is generally flat with sand dunes
bordering the coastline, and vegetation consists mainly of dry thorny
scrublands and lagoons. A total of 383 plant species have been recorded from
the park, including 6 endemics and 7 species considered as nationally
threatened. It is also home to 32 different species of mammals, 5 of which are
classified as threatened. For keen birdwatchers, the complex wetland system
harbours a rich bird life (approximately 197 species), including several
species of migratory species. This has led to the declaration of the park as
Sri Lanka’s first ‘Ramsar’ wetland – one of great international importance
especially for migratory species.&nbsp;&nbsp;&nbsp; </span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>&nbsp;</span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>GAL OYA NATIONAL PARK</span></b><span
style='color:red'> </span><span style='color:black'>lies in the southeast of
the island and to the west of Ampara. It was established in 1954 by the Gal Oya
Development Board mainly to protect the catchment area of the ‘Senanayake
Samudra’ Reservoir, and then handed over to the department of Wildlife
Conservation in 1965. About 45% of the park is covered by forest and savanna
areas take up a further 33%. The 25,900-hectare park has about 32 species of
mammals including common langur, endemic toque macaque, leopard, sloth bear,
elephant, wild boar, water buffalo and 3 species of deer.&nbsp; For birding
enthusiasts, approximately 150 of Sri Lanka’s 430 species of birds have been
observed in this region. </span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:black'>&nbsp;</span></b></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>HORTON PLAINS NATIONAL
PARK</span></b><span style='color:red'> </span><span style='color:black'>is
undoubtedly one of the world’s best nature reserves. It is characterized by a
beautiful landscape of rolling hills, covered in upper mountain forest and wet
patana grasslands. In general, the forests are seen on the hilltops or upper
slopes, and the grasslands in the valleys and lower slopes, eventually giving
way to wetland habitats. Spanning approximately 10,000 hectares the park is
also home to a wide variety of flora (57 species, 29 endemic to Sri Lanka) and
24 species of mammals such as elk, deer, giant squirrel, wild boar, hare, porcupine
and leopard. For bird enthusiasts, there are 87 species (14 of which are
endemic), including many migratory birds. The Plains also feature many
interesting attractions such as ‘Baker’s Falls’, ‘Chimmini Pool’ and the famous
‘World’s End’ (a 3700 ft sheer drop that offers fabulous views of the tea
estates below and all the way out to the distant southern coastline).</span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>&nbsp;</span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>KAUDULLA NATIONAL PARK</span></b><b><span
style='color:black'>,</span></b><span style='color:black'> Declared opened in
2002, is Sri Lanka’s newest National Park. Situated around the ancient Kaudulla
tank, the park provides a 6656-hectare elephant corridor, only 6 km from off
the main Habarana–Trincomalee road. With fantastic opportunities to see many
elephants at close range, the park has become a popular destination for
wildlife safaris and also viewing leopards, sambar deer and the occasional
sloth bear! You can also experience a catamaran rides on the tank.</span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>KUMANA NATIONAL PARK</span></b><span
style='color:black'>, (a.k.a. ‘Yala East’) Located in the southeast corner of
Sri Lanka, the 18,149 hectare is a well-known bird sanctuary where a multitude
of birds breed and roost. One of the most significant features of the park is
the ‘Kumana Villu’ - a 200-hectare natural swamp lake, fed by the ‘Kumbukkan
Oya’ through a half-mile long narrow channel. It is at this mangrove swamp that
many water birds nest in May and June. Regular sightings include pelicans,
painted storks, spoonbills, white ibis, herons, egrets and little cormorants.
The very rare black-necked stork has also been spotted. Besides the prolific
birdlife, Kumana is also home to some of the mammals found in the larger YALA
(West) park, such as elephants and leopards. The vegetation in Kumana consists
mainly of mangrove trees, kumbuk trees and the karan fern, as well as the open
marsh area. </span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>* For bird watching
enthusiasts, this park is an ‘absolute must’. </span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span lang=EN-GB style='color:black'>&nbsp;</span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span lang=EN-GB style='color:red'>MINNERIYA
NATIONAL PARK</span></b><span lang=EN-GB style='color:black'>, Located between
Habarana and Polonnaruwa, the 8,890 hectares National Park consists of mixed
forest and scrub areas and is home to sambar, leopards and elephants. However
the central feature of the park is the ancient Minneriya Tank (built in 3<sup>rd</sup>
century AD by King Mahasena). During the dry season (June to September), this
tank is an incredible place to observe the elephants that come to water and
graze, as well as the huge flocks of birds (cormorants and painted storks to
name a few) that come to feed in the shallow waters.&nbsp; </span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>&nbsp;</span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span lang=EN-GB style='color:red'>UDAWALAWE
NATIONAL PARK</span></b><span lang=EN-GB style='color:red'> </span><span
lang=EN-GB style='color:black'>is located approximately 200 km southeast of
Colombo. The 30,821-hectare dry zone game park has an annual rainfall of 1,524
mm and an average temperature of 29.4<sup>0</sup>C. It is most famous for
elephants (about 400 in total). During a visit, it is not unusual to see herds
– adults and young, feeding and bathing or playing in the water! In addition to
this main attraction, the park is home to many water buffalo, water monitor
lizards, sambar, monkeys and the occasional leopard, and an exciting location
for bird enthusiasts. A 4WD open-top safari is the only way to see all the
wonders this protected reserve has to offer, and our experienced and
knowledgeable nature guides will make this an unforgettable experience. </span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span lang=EN-GB style='color:black'>&nbsp;</span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span lang=EN-GB style='color:red'>WASGAMUWA
NATIONAL PARK</span></b><span lang=EN-GB style='color:black'>, located in the
districts of Matale and Polonnaruwa and bordered by the ‘Mahaweli’ and ‘Amban’
rivers, stretches an impressive 36,948 hectares. It was declared a National
Park in 1984, making it the only one in the Central Province. Home to a huge
variety of flora and fauna (23 species of mammals – including elephants and
bears, 143 birds, 8 amphibians, 17 fresh water fishes, 17 reptiles, 50
butterflies and 150 plants), this park is truly a paradise for the nature
lover. With </span><span style='color:black'>many small scale ancient tanks and
several other ruins and ancient religious sites the park has significant
historical importance. For keen hikers, it is also home to the ‘Sudu Kanda’
mountain range (about 470m above sea level).</span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>&nbsp;</span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>WILPATTU NATIONAL PARK</span></b><b><span
style='color:black'>,</span></b><span style='color:black'> is located 26 km
north of Puttalam (approximately 180 km north of Colombo), spanning an area
that runs inland from the northwest coast towards the ancient capital of
Anuradhapura (50 km to the east of the park).&nbsp; Covering an impressive 425
sq miles, the park is Sri Lanka’s largest. Having reopened in 2003 it is now an
increasingly popular eco-tourism destination. Wilpattu is a fairly thick dry
zone jungle interspersed with a number of flood plain lakes banked with
delicate white sands. It boasts an impressive variety of flora in huge expanses
of forest, and varied wildlife, including deer, elephants, wild boar, sloth
bears and leopards. Wilpattu National Park is world renowned for leopards. </span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>&nbsp;</span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>YALA NATIONAL PARK</span></b><b><span
style='color:black'>,</span></b><span style='color:black'> lays 24km northeast
of Tissamaharama and 290km from Colombo on the southeast coast of Sri Lanka,
spanning a vast 97,878 hectares over the Southern and Uva Provinces. The
vegetation in the park comprises predominantly of semi-arid thorny scrub,
interspersed with pockets of fairly dense secondary forest. Small patches of
mangrove vegetation are found along the coastal lagoons.&nbsp; </span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>&nbsp;The park is
renowned for the variety of its wildlife (most notably its many elephants) and
its fine coastline (associated with beautiful coral reefs). It boasts of a
large number of important cultural ruins, bearing testimony to earlier
civilizations and indicating that much of the area used to be populated and
well developed.</span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>&nbsp;</span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span lang=EN-GB style='color:black'>&nbsp;</span><b><u><span
style='color:red'>Special Interest Locations in Sri Lanka </span></u></b></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:black'>&nbsp;</span></b></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span lang=EN-GB style='color:red'>GALLE</span></b></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>Galle the seaside town is
famous for its well-preserved 17<sup>th</sup> century Dutch Fort, lace making
and ebony carvings. The journey to Galle is along the Palm fringed coast
passing Ambalangoda, famous for its mask makers and Hikkaduwa with its
incredibly beautiful coral gardens and exotic tropical fish. Seafood is a
specialty all the way. Another interesting place to visit is a Turtle Hatchery.</span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span lang=EN-GB style='color:black'>&nbsp;</span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span lang=EN-GB style='color:red'>HAMBANTOTA</span></b></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span lang=EN-GB style='color:black'>Hambantota is
the largest town on the south-eastern coast and is a well sheltered fishing
port. Just outside the town are saltpans where seawater is left to evaporate,
then carried by rail to salt factories. The town also boasts the best curd (a
local yoghurt made out of buffalo milk) and traditionally eaten with honey.</span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span lang=EN-GB style='color:black'>&nbsp;</span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>NEGOMBO (North of
Colombo)</span></b></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>The major beach resort on
the coast north of Colombo is Negombo, popularly known as the Fishing Village.
Here is an old world atmosphere of 17<sup>th</sup> century churches that line
the highway and forts. The feast of St. Anne is celebrated here in late July in
a carnival atmosphere. Like many other coastal towns in Sri Lanka, Negombo was
an important spice port long before the Portuguese set foot on the
island.&nbsp;&nbsp; The Negombo Lagoon is a good place to watch the Karava
fisherman at work. These fishermen bring their daily catch of fish, crabs and
prawns to the fish market. During the day they are seen mending their nets on
the beach.&nbsp; </span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>&nbsp;</span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span lang=EN-GB style='color:red'>SOUTH COAST</span></b></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span lang=EN-GB style='color:black'>(Mount
Lavinia, Wadduwa, Kalutara, Beruwela, Aluthgama, Bentota, Induruwa, Kosgoda,
Hikkaduwa, Galle, Unuwatuna, Koggala, Weligama, Dikwella, Tangalle)</span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span lang=EN-GB style='color:black'>All of these
towns have very good beaches and have an array of hotels to choose from. Along
the coast there are a variety of activities including water sports, a visit to
the botanical gardens, river boat rides (Bentota); a visit to a turtle hatchery
(Bentota, Induruwa &amp; Kosgoda); glass bottom boat rides to see corals,
snorkelling, diving and surfing (Hikkaduwa), or visiting a mask museum &amp;
factory and lace factory. (Ambalangoda – just before Hikkaduwa.) Of course,
seafood is predominant throughout.</span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span lang=EN-GB style='color:black'>&nbsp;</span></b></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>BUTTALA</span></b><span
style='color:red'>, </span><span style='color:black'>the area surrounding
Buttala is part of the Monaragala district in southeastern Sri Lanka. According
to the 5th century Buddhist chronicle “Mahavamsa”, Buttala itself was situated
on the northern border of King Dutugemunu's Ruhunu Kingdom as a defense base to
be used on his way to Anuradhapura, making it a location of considerable
cultural and historical importance. The landscape is tremendously varied with
abandoned chena fields, irrigation tanks, dense tall dry zone forests, thorny
scrub jungle, many flowering trees and rare tropical woods (such as the
protected ebony tree). The region also provides an ideal base from which to
explore the famous Yala National Park. The park was originally a reserve for
hunters, but it was declared a protected area in 1900, a sanctuary in 1909, and
a national park in 1938. It is a fascinating agglomeration of protected areas
and consists of a variety of different ecosystems; essential for the diverse
wildlife that inhabits it. </span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:black'>&nbsp;</span></b></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>DAMBANA</span></b><span
style='color:black'> is a remote jungle village of indigenous people, situated
about 300km from Colombo. The population of these indigenous ‘Veddha’ people
now consists of only about 350 families, a number significantly reduced from a
once thriving community.&nbsp; The ‘Veddha’ are so influenced by the imposing
dominant culture that they risk losing their traditions and what has been their
livelihood for centuries. However, they remain determined that their way of
life will continue into future generations. The Chief himself told us on a
recent visit to the village that his main worry is that he may not be able
prevent the ‘Veddha’ traditions from dying out during his own lifetime.
Visitors to this fascinating village have the opportunity to meet and chat with
the people who live there, learn about their traditions, and gain a unique
insight into their way of life. Your visit will also be of a direct financial
benefit to the community, as well as paying them a rent for the campsite and a
fee for their involvement in the programme</span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>&nbsp;</span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span lang=EN-GB style='color:red'>KALU GANGA</span></b><span
lang=EN-GB style='color:red'> </span><span lang=EN-GB style='color:black'>is a
relatively slow flowing river for most of the way, so there is no need for any
specialist canoeing skills. Along the route you will be able to see many
vegetation types and changing landscapes. Just passing the populated Ratnapura
town area, the riverbank environment becomes more scenic and peaceful, with
lonely sand collectors’ houses surrounded by thick vegetation and palm trees!
During most of the ride the only noises you will hear are the birdcalls and the
sound of your paddle dipping into the calm water. For a bit more adventure, it
is possible to continue down the river where you will negotiate some basic
rapids.&nbsp; The journey finishes at the west coast town of Kalutara, which is
famous for its impressive ‘Gangatilaka Vihara’ (Buddhist shrine), fine basket
ware and the best mangosteens in Sri Lanka!&nbsp; </span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span lang=EN-GB style='color:black'>&nbsp;</span></b></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span lang=EN-GB style='color:red'>SINHARAJA
RAINFOREST</span></b><span lang=EN-GB style='color:red'> </span><span
lang=EN-GB style='color:black'>is a world heritage site, which can also be
described as a Tropical Lowland Rainforest, it is undoubtedly a rich treasure
trove of nature with a great diversity of habitats and a vast repository of Sri
Lanka’s endemic species found nowhere else in the world. Bird Watching in this
ecosystem is particularly interesting because it is home to 95% of the endemic
birds of Sri Lanka! Named as a world heritage site in 1989, this lowland evergreen
rain forest is steeped in deep legend and mystery. The word ‘Sinharaja’ means,
Lion (Sinha) King (Raja), and it is popular belief that the legendary origin of
the Sinhala people is from the union between a princess and the lion king who
once lived in the forest!</span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span lang=EN-GB style='color:black'>&nbsp;</span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span lang=EN-GB style='color:red'>MIHINTALE</span></b></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span lang=EN-GB style='color:black'>The story of
Mihintale is a story etched deep in the national consciousness. It was at
Mihintale that, King Devanampiyatissa while on a deer hunt with his attendants
encountered the grandest event in the history of this land – the advent of
Buddhism.</span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span lang=EN-GB style='color:black'>The Mihintale
Mountain, less than 13 km from Anuradhapura has never ceased to fascinate both
local and foreign visitors since it is the cradle of Buddhism in Sri Lanka.
Visitors can join in the ascent of one of the world’s most antiquated stone
stairway climbing 1840 steps to reach the summit.</span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>&nbsp;</span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span lang=EN-GB style='color:red'>MATALE</span></b></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>The main tourist
attraction in Matale is the handicrafts and Spice Gardens where one can view
various spices that have been grown. A guided tour explains the herbal and
medicinal value of these spices.&nbsp; Matale is also known to have some of the
best craftsmen in the country. Having learnt the work from their ancestors,
these talented craftsmen strive to create works of art making use of the
traditional methods to suit the modern day living styles. Apart from spices and
handicrafts, the colourful batiks (a traditional Sri Lankan form of hand
painted designs onto cloth) are also a great attraction.</span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span lang=EN-GB style='color:black'>&nbsp;</span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span lang=EN-GB style='color:red'>PINNAWELA</span></b></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>Pinnawela is situated 52
km away from Colombo. It is an orphanage for baby elephants created by the
Department of National Zoological Gardens. The orphanage was established to
feed, nurse and house young elephants found abandoned by their mothers. Other
inmates are those displaced from their natural environs by development projects
or those found wounded. It has been recorded that a few baby elephants have
been bred here in captivity.</span></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span lang=EN-GB style='color:black'>&nbsp;</span></b></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>RATNAPURA</span></b></p>

<p class=MsoNormal style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span lang=EN-GB style='color:black'>Visit the
gemming area, panning of gems, watching men at work with their age-old customs
and rituals. Experience the thrill of unearthing a priceless treasure. Visit a
gem cutting and polishing centre and the gem museum. View an exhibition of Sri
Lankan Gems.</span></p>



