<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:black'>&nbsp;</span></b><b><i><span
style='color:red'>Cultural sites in Thailand</span></i></b></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><i><span style='color:red'>&nbsp;</span></i></b></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>Every region of Thailand
be it in the north, south, central or northeast, has revealed a long legacy of
civilization. Evidence of this rich cultural heritage can still be seen today. </span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>The two most prominent
'must-see' historical sites in Thailand are Sukhothai Historical Park and
Ayutthaya Historical Park. Both places are designated as world heritage sites
and both attract millions of admirers annually, who come to see the evidence of
Siam's former glory. </span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>Sukhothai and Ayutthaya
are considered the two most significant ancient kingdoms in Thai history, since
Siam was united as a nation some 750 years ago. Both kingdoms had
well-structured Buddhist communities, with advanced artistic and cultural
development.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>In Sukhothai, visitors
can see well-restored ruins of the royal palaces, Buddhist temples, the city
gates, walls, moats, dams, ditches, ponds, canals and the water dyke control
system, which represented the magical and spiritual center of the kingdom.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>For travelers with more
limited time who don't want to travel too far from Bangkok, the Ayutthaya
Historical Park is a perfect choice. Just 76 km north of Bangkok, this site
reveals the glory of the Ayutthaya Empire, which held sway in the region for
more than 400 years. The Ayutthaya historical park contains the ruins of
ancient Buddhist temples and royal palaces in a well-organized city structure. </span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>Ayutthaya was the capital
of Thailand from 1350 until it was ruthlessly sacked by the Burmese in 1767.
Although a shadow of its former glory, the magnificent ruins of the old city
can still be admired. Attractions include the ruins of the former Royal Palace
and many ancient temples, which form a complex spread over the vast area of the
historical park. </span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>Before or after touring
Ayutthaya Historical Park, don't forget to visit Bangkok's own historical
center. The city's Rattanakosin Island is rich in historical sites from the
early Bangkok period. Here, you can get a glimpse of how Ayutthaya might have
looked. At the time of constructing Bangkok, the goal of the architects was to
continue the cultural heritage of Ayutthaya. The art and architecture of the
temples and palaces therefore followed the style of Ayutthaya quite closely.
The most obvious indications of this can be seen in the Golden Mount at Wat
Saket, the Grand Place, the royal temples and Wat Arun, which imitated the
artistic designs of Ayutthaya. </span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>Today, if you look at
Rattanakosin, the original island of Bangkok, you will see a city tucked into a
bend in the river in the same way that Ayutthaya was. Its principal structures
are also aligned in similar fashion. </span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>Apart from the historical
remains of the true Thai kingdoms, you can see the historical arts of the Khmer
empire, which ruled over the region during the 10th to the 14th centuries AD. </span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>Anyone familiar with the
magnificent Angkor Wat in Cambodia's Siem Riep may be surprised to learn that
the Khmer empire once embraced the area of modern-day Thailand as well.
Historic artifacts of the Khmer civilization have been found mainly in the
lower part of the northeastern region of Thailand. </span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>Major Khmer complexes include
the Phimai Sandstone Sanctuary in Nakhon Ratchasima, Phanom Rung and Muang Tham
Sanctuary in Buriram and Khao Phra Viharn Sanctuary in Sisaket province. </span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>Like Angkor Wat itself,
these temples were all originally constructed as Hindu temples dedicated to
Vishnu and Shiva. It was under Jayavaraman VI that Buddhism replaced Hinduism
and thus the Hindu temples were converted to 'wat'. </span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>Their significance to
Thai Buddhists remained strong even with the decline of the Khmer empire. The
Khmer style heavily influenced the styles of the later Sukhothai and Ayutthaya
periods, most apparent in the architectural style of the Prang in Buddhist
temples.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>Phanom Rung Historical
Park in Buriram province is probably the most impressive Khmer complex in
Thailand. After 17 years of restoration, the temple complex is the best
restored of all the Khmer monuments in Thailand. The beauty of the main prang
of Phanom Rung lies not only in its plan but in the vibrant stone carvings
which cover large parts of the temple. Many lintels and pediments depict
episodes from Indian texts such as the Ramayana. </span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>In Nakhon Ratchasima,
Phimai sandstone sanctuary has been magnificently restored and renovated and
has been preserved as a national historical park. Built in the shape of a
rectangle, 665 meters wide and 1,030 meter long, it is the largest sandstone
sanctuary in Thailand. </span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>Khmer influence can be
seen as far away as Lopburi and Kanchanaburi. Evidence can be seen in the
Khmer-style Phra Prang Sam Yot in the central city of Lopburi and the remains
of the Prasat Muang Singh in Kanchanaburi, major attractions in their
respective provinces.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>&nbsp;Evidence found at
several archeological sites in Kanchanaburi indicates that the area has been
inhabited since the prehistoric era. In more recent times, the province has
acted as a buffer zone to hinder invasions from Burma, Thailand's long-time
rival to the west.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>Undoubtedly, Kanchanaburi
is best known for its role in the Second World War. Chosen by the Japanese as
the main supply route for an invasion westwards into British India,
Kanchanaburi was the scene of untold suffering and atrocities during the
construction of the notorious 'Death Railway'. The province still has many
sites to tell the story of the Japanese war machine and the countless Asians and
prisoners of war who perished because of it.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>Phetchaburi is the place
where the golden era of Ayutthaya lives on. Much visited by the Chakri monarchs
at the turn of the last century, the province offers the visitor a wealth of
historic temple sites and magnificent royal palaces.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>With its rich and
fascinating history, Chiang Mai province has a large number of memorable
historical sites to intrigue and enthrall even the most demanding of visitors.
There are numerous Thai temples or 'wats' (over 300 in the provincial capital
alone) dating from as far back as the 13th century. There are also many other
interesting sites such as museums, palaces, ancient cities, archaeological
sites, statues and monuments to be viewed.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>For centuries, Northern
Thailand has been the subject of numerous wars between Thailand and Burma, and
the diverse culture of the region is evident of this fact. </span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>In no aspect is this more
reflected than in the religious architecture of the region, and many of the
historical sites in Mae Hong Son are no exception to this rule. </span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>Most of the important
temples, such as Wat Phra That Doi Kong Mu, Wat Hua Wiang, Wat Chong Klang and
Chong Kham, have striking, white pagodas, a trait distinctively Burmese, and
many of Mae Hong Son's enshrined Buddha statues are dissimilar from those found
in other regions of the country.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>This Northeastern
province is an ideal destination for the history lover, as it offers some of
the finest Khmer artifacts found outside of Cambodia. Several museums in Khon
Kaen house fine collections Khmer art, and the ruins of numerous ancient Khmer
temples can be found in the countryside.</span></p>

