<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><i><span style='font-size:14.0pt;color:red'>PLACES
OF INTEREST AND THINGS TO SEE IN THAILAND</span></i></b></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><i><span style='color:red'>Thailands Beaches.</span></i></b></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>&nbsp;</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>Visiting a Thailand beach
or island is the preferred holiday for hundreds of thousands of visitors to
Thailand every year. Thailand is blessed with natural beauty and its islands
are amongst the most scenic and beautiful in the world. Likewise, each Thai beach,
such as Karon Beach in Phuket, Chaweng Beach on Koh Samui, or picturesque Maya
Bay on Koh Phi Phi, is simply stunning, and many feature powdery sand, crystal
clear water, and postcard-perfect scenery. <br/>
<br/>
Each Thai beach and island has its own character and identity and therefore
draws a specific type of visitor.&nbsp; Each coastal area contains a slice of
heaven suitable for a different style of traveler:&nbsp; The west coast of
Thailand, along the Andaman Sea, features beaches that appeals to every type of
traveler, including the activity-filled resort island of Phuket; the popular
backpacker beaches of Koh Phi Phi, Koh Lanta, and Krabi; the family friendly,
laid back, and pristine coast of Khao Lak (the launching point for trips to the
spectacular Similan Islands); and the remote, undeveloped islands of the far
south.<br/>
<br/>
Along the Gulf coast, the resort island of Koh Samui lies nearby the natural
splendor of Koh Phangan and the scuba diving paradise of Koh Tao.&nbsp; Closer
to Bangkok are the popular resort town of Hua Hin, a favorite among Thais, and
its quieter neighbor Cha Am.&nbsp; Finally, to the east of Thailand, the
northern Gulf features Bangkok weekend getaway Koh Samet, and the up-and-coming
resort island of Koh Chang, which has both upscale resorts and budget beach
bungalows.<br/>
<br/>
Certain Thai beaches and islands, like Koh Tarutao National Park, offer limited
accommodation and facilities and draw more adventurous travelers who are
looking for a more 'back to basics' holiday experience. Others, such as Kamala
Beach in Phuket, offer world class facilities (accommodation, restaurants,
nightlife, etc) to entice visitors with bigger budgets who require creature
comforts. It is important to note that this diversity exists not only between
the islands, but between different beaches as well. Whereas one Thai beach
might offer raucous entertainment, another a few kilometers away on the same
island might only draw those looking for a quiet holiday. <br/>
<br/>
Phuket, Thailand's largest island, is a perfect example of this contrast.&nbsp;
Phuket is certainly the most developed Thai island, having been the first Thai
beach resort destination.&nbsp; Located on the Andaman coast, Phuket contains
numerous beaches, including the activity filled Patong beach, with its exciting
nightlife, and the more family friendly Karon and Kata beaches.&nbsp; Across
the island are luxurious five-star resorts and a wealth of Thai spas that serve
to pamper visitors on any budget.&nbsp; In addition to a lush, tropical
interior that features a variety of exotic wildlife, Phuket is an ideal
location for day trips to nearby islands, such as Koh Phi Phi, a favorite
destination for scuba divers, and Phang Nga bay, where visitors can snorkel,
kayak, and visit iconic James Bond Island (Koh Tapu).&nbsp; Phuket is easily
accessible via its international airport that connects domestically with
Bangkok, Koh Samui, and Chiang Mai.&nbsp; <br/>
<br/>
Koh Samui is the prime island attraction in the Gulf of Thailand.&nbsp; The
most popular beach, Chaweng, features accommodation from five-star luxury
resorts to affordable beach bungalows, and dining includes fine dining on
international cuisine and casual beachside seafood barbeques.&nbsp; Samui is
both family friendly and budget oriented with a host of activities, some of
Thailand's finest spas, and is conveniently located nearby some of Thailand's
finest diving off neighboring Koh Tao.&nbsp; Samui International Airport
connects domestically to Bangkok, Krabi, Phuket, and Chiang Mai. <br/>
<br/>
Krabi is a province on the mainland Andaman coast, near Phuket.&nbsp; In
addition to popular beachside resort areas, such as Railey Beach, Krabi
includes a number of spectacular islands, such as the Phi Phi Islands and Koh
Lanta, off of which some of Thailand's most popular scuba diving sites are
found.&nbsp; </span></p>

