OF INTEREST AND THINGS TO SEE IN NEW ZEALAND</span></i></b></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><u><span lang='EN-GB' style='color:red'>Beaches in
New Zealand</span></u></b></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:black'>&nbsp;</span></b></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>&nbsp;Waiheke Island's
Onetangi Bay:</span></b><span style='color:red'> </span><span style='color:
black'>Stand on the bay's wide stretch of golden sand, and you can see for
miles. On a clear day, throw yourself down into the sand and gaze at the steep
pinnacles of Great Barrier Island and Little Barrier, off in the hazy distance.
There might even be a few glimpses of the Coromandel in between deliciously
warm swims.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Karikari Peninsula's
Beaches:</span></b><span style='color:red'> </span><span style='color:black'>This
is the Far North at its subtropical best -- endless sweeps of sparkling white
sand lapped by crystal-clear, azure-blue waters. And from Tokerau Beach to
Rangiputa to Matai Bay, you could have miles of it to yourself for
beachcombing, sunbathing, and swimming (with care).</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Coopers Beach:</span></b><span
style='color:red'> </span><span style='color:black'>Partly shaded by a bank of
red-emblazoned pohutukawa trees, how could you not be content to stretch out
here? Exercise? A walk to the water should do it!</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Hot Water Beach:</span></b><span
style='color:red'> </span><span style='color:black'>Don't ask us why, or how,
but if you get here 2 hours before or after low tide, you can hollow out a spot
on the beach for yourself, and then wait for natural hot water to seep up
through the sand. A natural spa experience without paying a cent!</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Mount Maunganui's Ocean
Beach:</span></b><span style='color:red'> </span><span style='color:black'>Surf,
sand, and sun -- some people never want more than that. Throw on a bit of
suntan lotion, a pair of sunglasses, and a skimpy swimsuit, and you'll be able
to mix in with the best of the bronzed bodies that make an annual pilgrimage to
this perennial beach and surf favorite.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Kaiteriteri Beach:</span></b><span
style='color:red'> </span><span style='color:black'>Half of Canterbury makes a
beeline for these blissful shores every summer. It's not expansive -- in fact
it's quite small, but perfectly formed nonetheless, and there's a busy vacation
atmosphere with packed campgrounds and holiday houses. And with Nelson's
endless hours of sunshine, who could complain about size?</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Abel Tasman National
Park's Beaches:</span></b><span style='color:red'> </span><span
style='color:black'>Bush-wrapped and locked between rocky headlands, these
idyllic golden patches from Marahau north to Totaranui are accessible by sea
only. That's what makes them so special. Gliding through the turquoise waters
in your sea kayak, with curious seals to keep you company, you can take your
pick of isolated havens. <br/>
<br/>
</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><u><span style='color:red'>Cultural Sites in New
Zealand</span></u></b></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>&nbsp;</span></b></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Alberton:&nbsp; </span></b><span
style='color:black'>A classic colonial mansion (1863), Alberton offers tours
and was used as a location for some scenes in <i>The Piano</i>. Bring a picnic
lunch and enjoy the grounds.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Auckland Museum:&nbsp; </span></b><span
style='color:black'>Dominating the Auckland Domain is this magnificent museum,
an imposing Greek temple with an impressive modern dome. Its comprehensive
display of Pacific Island and Maori artefacts on the ground floor deserves to
be on your 'must see' list. Highlights include a 25m war canoe and an extant
carved meeting house from the Thames area that you can enter (remove your shoes
first). Bookings are required for the museum highlights guided tour. Maori
gallery tours (same prices) take place at 11.30am and 2pm. Daily Maori cultural
performances provide a good (and good-humoured) introduction to things Maori.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Canterbury Museum : </span></b><span
style='color:black'>The absorbing Canterbury Museum has a wonderful collection
of items of significance to NZ. Highlights include the Maori gallery, with some
stunning <i>pounamu</i> (greenstone) pieces on display; the coracle in the
Antarctic Hall used by a group shipwrecked on Disappointment Island in 1907;
and a wide array of stuffed bird life from the Pacific and beyond. Don't miss
the statuesque Emperor penguin. Guided tours (donations appreciated) run from
3.30pm to 4.30pm on Tuesday and Thursday. Kids will enjoy the interactive
displays at Discovery (admission $2).</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Elms Mission
Station:&nbsp; </span></b><span style='color:black'>Built in 1847, Elms Mission
Station is the oldest building in the Bay of Plenty. Furnished in period style,
it sits among other well-preserved mission buildings in leafy grounds. The
spooky Mission Cemetery lies not far away at the intersection of Marsh Street
and Dive Crescent &#8722; good for a little epitaph-reading.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Fyffe House:&nbsp; </span></b><span
style='color:black'>Kaikoura's oldest surviving building is Fyffe House. Built
by Scotsman George Fyffe, cousin of Kaikoura's first European settler, Robert
Fyffe, it started life as a small cottage in 1842 (with whale vertebrae for
foundations) and was completed in 1860. There's plenty to see inside and out,
including the original brick oven, historical displays and gardens.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Howick Historical
Village: </span></b></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>This fascinating 'living'
museum will split visitors along 'Isn't it tacky/fascinating' lines, with its
costumed staff evoking the atmosphere of Auckland in the turbulent pioneering
era from the 1840s to the 1880s. There are over 30 buildings, many relocated
here from other parts of the region.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>The streets, the pond
with ducks and geese, and the village gardens are faithful reconstructions of
Victorian fashion, and there's a cafe with homemade goodies as well. There is a
theme day on the third Sunday of each month, with special events and displays
such as the blacksmith working at the forge, the 65th Regiment firing its
muskets, and maybe even a chance to see the school in session.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Mission House:&nbsp; </span></b><span
style='color:black'>The Mission House is the country's oldest wooden building
(1822) and contains some original fittings and chattels.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>New Zealand Cricket
Museum: </span></b><span style='color:black'>Cricket boffins will be bowled
over by the historical memorabilia at the New Zealand Cricket Museum.
Comprehensive displays cover the history and development of NZ cricket,
including the sport's arrival in the colonies and NZ's first test match in
1894. The original 1743 Addington bat is a showstopper.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Otago Museum: </span></b><span
style='color:black'>The modern and interactive Otago Museum explores Otago's
cultural and physical past and present, from geology and dinosaurs to the
modern day. The beautifully designed Tangata Whenua gallery houses an
impressive <i>waka taua</i> (war canoe), wonderfully worn old carvings and some
lovely <i>pounamu</i> (greenstone) works. This is one of the richest
repositories of Maori knowledge on the South Island. If you've already been out
on the peninsula admiring penguins and albatrosses, the museum's collection of
ancient and contemporary wildlife will fascinate. Children can explore at the
hands-on Discovery World , and there's a smart, newly expanded cafe with
surprisingly good food. Check their website for always-excellent temporary
exhibitions and special gallery talks.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Riverboat Centre</span></b><span
style='color:red'> </span><span style='color:black'>has historical displays,
but the crowds come for the <i>Waimarie,</i> the last of the Whanganui River's
paddle steamers. In 1900 the <i>Waimarie</i> was shipped in a box from England
then reassembled in Whanganui. After paddling the Whanganui for 50 years, she
sank ingloriously at her mooring in 1952. Submerged for 41 years, she was
finally raised, restored, then relaunched on the first day of the 21st century.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Stone Store:&nbsp; </span></b><span
style='color:black'>The Stone Store, next to the Mission House, is the oldest
stone building in NZ (1836). It sells interesting gifts as well as the type of
goods that used to be sold in the store - although these days you'll have a
hard time bartering pigs for muskets. Tours&nbsp; of the Mission House depart
from here and include entry to the displays on the 1st floor of the store.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Te Manawa</span></b><span
style='color:red'> </span><span style='color:black'>has merged a museum, art
gallery and science centre into one complex. Vast collections (around 55,000
items) join the dots between 'life, art and mind'. The museum has a strong
Maori focus, while the gallery's exhibits change frequently. Kids will get a
kick out of the hands-on exhibits at the science centre. The New Zealand Rugby
Museum will be relocating here as part of a wholesale redevelopment.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Te Papa: </span></b><span
style='color:black'>the 'Museum of New Zealand', is an inspiring, interactive
repository of historical and cultural artefacts. 'Te Papa Tongarewa' loosely
translates as 'treasure box'. The building dominates the Wellington waterfront
and has become a national icon - an innovative celebration of the essence of
NZ.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Waeckerle Cottage : </span></b><span
style='color:black'>The <i>Akaroa - an Historic Walk </i>booklet details a
walking tour starting at the 1876 Waeckerle Cottage and finishing at the old
Taylor's Emporium premises near the main wharf. The route takes in the old
wooden buildings and churches that give Akaroa its character. </span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Waikato Museum : </span></b><span
style='color:black'>The excellent Waikato Museum has five main areas: an art
gallery; interactive science galleries; Tainui galleries housing Maori
treasures, including the magnificently carved waka taua (war canoe) Te Winika;
a Hamilton history exhibition entitled 'Never a Dull Moment'; and a Waikato
River exhibition. The museum also runs a rigorous program of public events.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:black'>&nbsp;</span></b></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><u><span lang='EN-GB' style='color:red'>Zoos,
Parks &amp; Botanical Gardens in New Zealand </span></u></b></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span lang='EN-GB' style='color:black'>&nbsp;</span></b></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Auckland Domain:&nbsp; </span></b><span
style='color:black'>Covering about 80 hectares, the green swathe of the
Auckland Domain contains sports fields, interesting sculpture, formal gardens, wild
corners and the Wintergarden, with its fernery, tropical house, cool house,
cute cat statue and neighbouring cafe. The mound in the centre of the park is
all that remains of Pukekaroa, one of Auckland's volcanoes. At its humble peak,
a totara surrounded by a palisade honours the first Maori king. Admirers of the
male form should check out the Domain's grafton Gate, a wonderful art-deco
sandstone construction topped with a larger-than-life bronze nude. </span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Auckland Zoo: </span></b></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>This is an excellent
modern zoo with spacious, natural compounds. The infrared lighting of the
nocturnal house offers a rare chance to see kiwi fossicking about. The big
foreigners tend to steal the attention from the timid natives, but if you can
wrestle the kids away from the tigers and elephants you'll find tuataras and a
large selection of native birds.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Botanic Gardens: </span></b><span
style='color:black'>The Botanic Gardens comprise 30 riverside hectares planted
with 10,000-plus specimens of indigenous and introduced plants. There are
conservatories and thematic gardens to explore, lawns to sprawl on, and a cafe
at the Botanic Gardens visitors centre. Get the kids active in the playground
adjacent to the cafe. Guided walks depart daily from the Canterbury Museum, or
you can ride around the gardens in the electric 'Caterpillar' train. Hop-on,
hop-off tickets are valid for two days and include a commentary.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Underwater Observatory :
</span></b><span style='color:black'>Underwater Observatory has six giant
windows showcasing life under the lake. Brown trout abound, and based on their
generous size, look to be impossibly well fed. Keep a keen eye out for
freshwater eels and the favourite of the kiddies - the scaup (diving ducks),
which dive down and swim right past the windows.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Wellington Botanic
Gardens : </span></b><span style='color:black'>The expansive, hilltop
Wellington Botanic Gardens can be conveniently visited via a cable-car ride.
The hilly 25-hectare gardens boast a tract of original native forest along with
varied collections including a beaut rose garden and international plant
collections. Add in fountains, a cheerful playground, sculptures, duck pond,
cafe, magical city views and much more, and you've got a grand day out. The
gardens are also accessible from the Centennial Entrance on Tinakori Rd .</span></p>

