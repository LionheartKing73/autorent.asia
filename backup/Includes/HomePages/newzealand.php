<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Cable Car:&nbsp; </span></b><span
style='color:black'>One of Wellington's most famous attractions is the little
red cable car that clanks up the steep slope from Lambton Quay to Kelburn. At
the top are a cafe, the Wellington Botanic Gardens, an observatory and the
small-but-nifty Cable Car Museum, which tells the cable car's story since it
was built in 1902 to open up hilly Kelburn for development. Take the cable car
back down the hill, or ramble down through the Botanic Gardens (a 30- to
60-minute walk, depending on your wend).</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Pelorus Mail Run: </span></b><span
style='color:black'>Popular full-day boat cruise through the far reaches of
Pelorus Sound on a genuine NZ Post delivery run. Bookings essential; BYO lunch.
Picton pick-up and drop-off available.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Tamaki Maori Village: </span></b><span
style='color:black'>Maori culture is a major drawcard in Rotorua and, although
some find it heavily commercialised, it's a great opportunity to learn more
about the indigenous culture of NZ. The two big activities are concerts and <i>hangi</i>
meals, often packaged together in an evening's entertainment that features the
famous <i>hongi</i>, <i>haka</i>, and <i>poi</i> dances.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><span style='color:black'>An established favourite
is Tamaki Maori Village, which does an excellent twilight tour to a <i>marae</i>
(meeting house) and Maori village 15km south of Rotorua. Buses collect from the
Hinemaru St booking office and also from most accommodation, and feature a
briefing on <i>marae</i> protocol by a Maori 'entertainer' (aka hilarious bus
driver). The concert is followed by a meat-heavy <i>hangi</i>.</span></p>

<p  style='margin-bottom:0cm;margin-bottom:.0001pt;line-height:
normal;text-autospace:none'><b><span style='color:red'>Tongariro River
Rafting:&nbsp; </span></b><span style='color:black'>The Tongariro River has
some superb Grade III rapids for river rafting, as well as Grade I stretches
suitable for beginners in the lower reaches during summer. Tongariro River
Rafting can start you off with a three-hour trip on Grade II Tongariro rapids
or take you on a full day's raft fishing (summer only, price on enquiry). The
company also hires out mountain bikes and runs guided and unguided biking trips
on the 42 Traverse, Tongariro River Track, Moerangi Station, Tree Trunk Gorge
and Fishers Track.</span></p>



