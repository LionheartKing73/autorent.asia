Calibri;mso-bidi-font-family:Calibri;color:red'>Padang &amp; Around: </span></b><span
style='mso-bidi-font-size:11.0pt;mso-ascii-font-family:Calibri;mso-hansi-font-family:
Calibri;mso-bidi-font-family:Calibri;color:black'>The open field of the Padang
is where flannelled fools play cricket in the tropical heat. At the opposite
end of the field is the Singapore Recreation Club. This rather prosaic spot has
darker historical significance, as it was here that the invading Japanese
herded the European community together before marching them off to Changi
prison. Apart from the Singapore Recreation Club (which looks like something
made from kids' building blocks), the Padang is flanked by a handsome
collection of colonial buildings and assorted monuments. At the Padang's
southern end, the Victoria Theatre &amp; Concert Hall (1862), once the town
