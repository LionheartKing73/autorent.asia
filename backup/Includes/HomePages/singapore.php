L'Europe, which once outshone the Raffles as Singapore's premier hotel.
Situated next door, and even newer, is the Foster &amp; Partners-designed
Supreme Court, which opened in 2005. City Hall, with its classical facade of
Corinthian columns, is located next to the Supreme Court and dates from 1929.
