old shophouse, the Chinatown Heritage Centre is an engaging museum focusing on
the arduous everyday lives of Singapore's Chinese settlers. Reconstructed
living environments are festooned with artefacts. The cramped quarters of
shophouse living are decked out with startling reality .</span></p>

